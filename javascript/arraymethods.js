// push -->it use to push the value in the end
let pu = [1,2,3,4,5]
pu.push(6)
console.log(pu);

// pop -->it use to remove the end of the value

let po = [1,2,3,4,5,6];
po.pop()
console.log(po);

// shift --> it use to remove the starting value  

let sh = [0,1,2,3,4,5];
sh.shift()
console.log(sh);

// unshift --> it use to add some value in the front

let un = ["java","css","git","api"];
un.unshift("javascript")
console.log(un);

